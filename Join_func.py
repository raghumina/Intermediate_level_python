# Join function in python
# generally join function is used in list set tuples when we need to join the strings or other data type
# for example

list1  = ["tom","cruz","hanary","cavill","johnny","deep"]
for names in list1:
    print(names)

# now we need to join the names

a = " and ".join(list1)
print(a)
a1 = ", ".join(list1)
print(a1)